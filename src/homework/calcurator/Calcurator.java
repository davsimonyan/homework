package homework.calcurator;

public class Calcurator {


    double plus(double x, double c) {
        return x + c;
    }

    double minus(double x, double c) {
        return x - c;
    }

    double multiply(double x, double c) {
        return x * c;
    }

    double divide(double x, double c) {
        return x / c;
    }
}


